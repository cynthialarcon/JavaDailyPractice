public class WhileCicle {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int count = 0;
        int limit = 11;
        while (count < limit){ // until 9 as it is < .
            System.out.println("Counting: " + count);
            count ++;
        }
    }
    
}
